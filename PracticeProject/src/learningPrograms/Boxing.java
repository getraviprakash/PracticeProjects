package learningPrograms;

public class Boxing {

	public static void main(String[] args) {
		
		Integer i = new Integer(5);		//Autoboxing
		int i1 = i;						//Unboxing
		System.out.println(i);
		System.out.println(i1);
	
		Character c = 'a';
		char c1 = c;
		System.out.println(c);
		System.out.println(c1);
	
	}

}
